    private static void carregaTabela() {

        List<String[]> valores = new ArrayList<>();

        try {
            BufferedReader br = new BufferedReader(new FileReader("airports.dat"));

            String header = br.readLine();

            String[] colunas = header.split(",");

            String linha;
            while ((linha = br.readLine()) != null) {

                String[] indices = linha.split(",");
                valores.add(indices);
            }

            br.close();

            DefaultTableModel model = new DefaultTableModel(valores.toArray(Object[][]::new), colunas);

            jTable1.setModel(model);
                  
        } catch (Exception e) {
                
            e.printStackTrace();       
        }
    }
    
